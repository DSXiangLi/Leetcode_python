# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class Solution:
    def largestValues(self, root: Optional[TreeNode]) -> List[int]:
        if not root:
            return []
        maxval = []
        stack=[root]
        while stack:
            l = len(stack)
            tmp = []
            for i in range(l):
                node =stack.pop(0)
                if node.left:
                    stack.append(node.left)
                if node.right:
                    stack.append(node.right)
                tmp.append(node.val)
            maxval.append(max(tmp))
        return maxval

